# list = used ot store multiple items in a single variable

food = ["pizza", "burger", "pasta"]

# To print entire list 
print(food)

# To acces specific element of list
print(food[1]) 

# List funcs:
food.append("ice cream")
food.remove("burger")
food.pop()
food.insert(0,"cake")
food.sort()
food.clear()

# To print entire list 
for i in food:
    print(i)