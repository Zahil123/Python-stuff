import math

pi = 3.14

# Round a number
print(round(pi))

# round number up
print(math.ceil(pi))

# round number down
print(math.floor(pi))

# absolute value (how far away a number is from 0)
print(abs(pi))

# Exponentiation 
print(pow(pi,2))

# Sqrt
print(math.sqrt(pi))

# Largest from a collection of nos
x = 1
y = 2
z = 3
print(max(x,y,z))

# Smallest from a collection of nos
print(min(x,y,z))
